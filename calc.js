function km_to_m(x)
{
    return x * 1000;
}
function m_to_km(x)
{
    return x / 1000;
}
function m_to_cm(x)
{
    return x * 100;
}
function cm_to_m(x)
{
    return x / 100;
}
exports.km_to_m = km_to_m(x)
exports.m_to_km = m_to_km(x)
exports.m_to_cm = m_to_cm(x)
exports.cm_to_m = cm_to_m(x)